// MAIN LOOKUP
// ============================================================
async function lookupReps() {
  const zipInput = document.getElementById('zipInput');
  const errorEl = document.getElementById('zipError');
  const resultsEl = document.getElementById('results');
  const btn = document.getElementById('lookupBtn');
  const zip = zipInput.value.trim();
  errorEl.textContent = '';

  if (!/^\d{5}$/.test(zip)) {
    errorEl.textContent = '⚠ Please enter a valid 5-digit zip code';
    return;
  }

  const coords = KS_ZIP_CENTROIDS[zip];
  if (!coords) {
    errorEl.textContent = '⚠ Zip code not found in Kansas. Please enter a Kansas zip code.';
    return;
  }

  btn.innerHTML = '<span class="btn-text">Looking up...</span>';
  btn.disabled = true;
  resultsEl.innerHTML = '<div class="no-results">Finding your representatives...</div>';

  try {
    const [lat, lng] = coords;
    const reps = await fetchRepsFromOpenStates(lat, lng);
    if (!reps || reps.length === 0) {
      resultsEl.innerHTML = noRepsFound(zip);
      return;
    }
    renderResults(reps, zip, resultsEl);
  } catch (err) {
    console.error('Lookup error:', err);
    resultsEl.innerHTML = apiError(zip);
  } finally {
    btn.innerHTML = '<span class="btn-text">Look Up</span><span class="btn-arrow">→</span>';
    btn.disabled = false;
  }
}

// ============================================================
// OPENSTATES GEO API
// Register free key at: https://open.pluralpolicy.com/accounts/profile/
// ============================================================
async function fetchRepsFromOpenStates(lat, lng) {
  const url = `https://v3.openstates.org/people.geo?lat=${lat}&lng=${lng}&jurisdiction=ks&include=other_names`;
  const response = await fetch(url, {
    headers: { 'X-API-KEY': OPENSTATES_API_KEY }
  });
  if (!response.ok) throw new Error(`OpenStates API error: ${response.status}`);
  const data = await response.json();
  if (!data.results) return [];

  return data.results.map(person => {
    const role = person.current_role || {};
    return {
      name: person.name,
      chamber: role.org_classification === 'lower' ? 'House' : 'Senate',
      party: person.party,
      district: role.district,
      office: `District ${role.district}`
    };
  });
}

// ============================================================
// VOTE MATCHING
// ============================================================
function findVoteRecord(apiName, chamber) {
  const pool = chamber === 'House' ? SB244_VOTES.house : SB244_VOTES.senate;
  const norm = s => s.toLowerCase().replace(/[^a-z\s]/g, '').trim();
  const apiLast = norm(apiName).split(' ').pop();
  return pool.find(r => norm(r.name) === apiLast || norm(apiName).includes(norm(r.name))) || null;
}

// ============================================================
// RENDER
// ============================================================
function renderResults(reps, zip, container) {
  const matched = reps.map(rep => ({ ...rep, voteRecord: findVoteRecord(rep.name, rep.chamber) }));
  let html = `<div class="results-header">Results for zip ${zip} — ${matched.length} Kansas state legislator${matched.length !== 1 ? 's' : ''} found</div>`;

  matched.forEach((rep, i) => {
    const vote = rep.voteRecord?.vote || 'UNKNOWN';
    const voteDisplay = vote === 'YEA' ? 'YES' : vote === 'NAY' ? 'NO' : '?';
    const voteMeaning = vote === 'YEA' ? 'Voted to harm trans Kansans' : vote === 'NAY' ? 'Voted to protect trans Kansans' : 'Vote not found';
    const cardClass = vote === 'YEA' ? 'rep-card--yea' : vote === 'NAY' ? 'rep-card--nay' : '';
    const voteClass = vote === 'YEA' ? 'vote-value--yea' : vote === 'NAY' ? 'vote-value--nay' : '';
    const repLast = rep.voteRecord?.name || rep.name.split(' ').pop();
    const opposition = OPPOSITION_CANDIDATES[`${repLast}-${rep.chamber}`];

    html += `<div class="rep-card ${cardClass}">
      <div class="rep-card-header">
        <div class="rep-info">
          <div class="rep-chamber">Kansas ${rep.chamber} · ${rep.office || ''}</div>
          <div class="rep-name">${rep.name}</div>
          <div class="rep-party">${rep.party || ''}</div>
        </div>
        <div class="rep-vote-badge">
          <span class="vote-label">SB244 Vote</span>
          <span class="vote-value ${voteClass}">${voteDisplay}</span>
          <span class="vote-meaning">${voteMeaning}</span>
        </div>
      </div>`;

    if (vote === 'YEA') {
      html += `<div class="rep-actions">
        <div class="rep-actions-label">Take action against ${rep.name.split(' ').pop()}</div>
        <div class="action-buttons">
          ${opposition
            ? `<a href="${opposition.url}" target="_blank" rel="noopener" class="action-btn action-btn--oppose">Donate to ${opposition.name} ↗</a>`
            : `<a href="https://www.ksdp.org/run" target="_blank" rel="noopener" class="action-btn action-btn--oppose">Run Against Them / Find a Challenger ↗</a>`}
          <button class="action-btn action-btn--donate" onclick="toggleDonate('donate-${i}')" type="button">Donate in Their Name to a Pro-Trans Org</button>
        </div>
      </div>
      <div class="donate-dropdown" id="donate-${i}">
        <div class="donate-dropdown-label">Choose an org — donated in ${rep.name.split(' ').pop()}'s name:</div>
        <div class="donate-org-list">
          ${ORGS.map(org => `<a href="${org.url}" target="_blank" rel="noopener" class="donate-org-link"><span class="org-name">${org.name}</span><span style="display:flex;align-items:center;gap:1rem;font-size:0.65rem;color:var(--gray)">${org.desc}<span class="org-arrow">↗</span></span></a>`).join('')}
        </div>
      </div>`;
    } else if (vote === 'NAY') {
      html += `<div class="rep-thanks">✓ ${rep.name} voted NO on SB244. Thank them — help re-elect them.</div>`;
    } else {
      html += `<div class="rep-thanks" style="color:var(--gray)">Vote record not found. Check <a href="https://www.kslegislature.gov/li/b2025_26/measures/sb244/" target="_blank" rel="noopener" style="color:var(--white)">kslegislature.gov ↗</a></div>`;
    }
    html += `</div>`;
  });

  container.innerHTML = html;
  setTimeout(() => container.scrollIntoView({ behavior: 'smooth', block: 'start' }), 100);
}

